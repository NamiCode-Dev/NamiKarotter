function normalizeThemePalettes(themes) {
  const neutralKeys = [
    "--neutral-50",
    "--neutral-100",
    "--neutral-200",
    "--neutral-300",
    "--neutral-400",
    "--neutral-500",
    "--neutral-600",
    "--neutral-700",
    "--neutral-800",
    "--neutral-900",
    "--neutral-950"
  ];

  const clampChannel = (value) => Math.max(0, Math.min(255, Math.round(value)));
  const normalizeHex = (value) => {
    const match = String(value || "").trim().match(/^#([0-9a-f]{3}|[0-9a-f]{6})$/i);
    if (!match) return null;

    const hex = match[1];
    if (hex.length === 3) {
      return `#${hex[0]}${hex[0]}${hex[1]}${hex[1]}${hex[2]}${hex[2]}`.toLowerCase();
    }
    return `#${hex}`.toLowerCase();
  };

  const hexToRgb = (value) => {
    const hex = normalizeHex(value);
    if (!hex) return null;

    return {
      r: parseInt(hex.slice(1, 3), 16),
      g: parseInt(hex.slice(3, 5), 16),
      b: parseInt(hex.slice(5, 7), 16)
    };
  };

  const rgbToHex = ({ r, g, b }) => {
    const toHex = (channel) => clampChannel(channel).toString(16).padStart(2, "0");
    return `#${toHex(r)}${toHex(g)}${toHex(b)}`;
  };

  const mixHex = (from, to, amount) => {
    const fromRgb = hexToRgb(from);
    const toRgb = hexToRgb(to);
    if (!fromRgb || !toRgb) return normalizeHex(from) || normalizeHex(to) || from;

    const ratio = Math.max(0, Math.min(1, amount));
    return rgbToHex({
      r: fromRgb.r + (toRgb.r - fromRgb.r) * ratio,
      g: fromRgb.g + (toRgb.g - fromRgb.g) * ratio,
      b: fromRgb.b + (toRgb.b - fromRgb.b) * ratio
    });
  };

  const relativeLuminance = (value) => {
    const rgb = hexToRgb(value);
    if (!rgb) return 0;

    const linear = [rgb.r, rgb.g, rgb.b].map((channel) => {
      const normalized = channel / 255;
      return normalized <= 0.03928
        ? normalized / 12.92
        : Math.pow((normalized + 0.055) / 1.055, 2.4);
    });

    return 0.2126 * linear[0] + 0.7152 * linear[1] + 0.0722 * linear[2];
  };

  const buildNeutralScale = (colors, isLightTheme) => {
    const appBg = normalizeHex(colors["--app-bg"]) || "#f8fafc";
    const surfaceCard = normalizeHex(colors["--surface-card"]) || appBg;
    const surfaceSoft = normalizeHex(colors["--surface-soft"]) || appBg;
    const surfaceElevated = normalizeHex(colors["--surface-elevated"]) || surfaceSoft;
    const textPrimary = normalizeHex(colors["--text-primary"]) || (isLightTheme ? "#111827" : "#f8fafc");
    const textSecondary = normalizeHex(colors["--text-secondary"]) || textPrimary;
    const textMuted = normalizeHex(colors["--text-muted"]) || textSecondary;

    if (isLightTheme) {
      return [
        mixHex(surfaceCard, appBg, 0.32),
        mixHex(surfaceCard, appBg, 0.68),
        mixHex(surfaceCard, surfaceSoft, 0.92),
        mixHex(surfaceSoft, textMuted, 0.24),
        mixHex(appBg, textMuted, 0.52),
        textMuted,
        textSecondary,
        mixHex(textSecondary, textPrimary, 0.5),
        textPrimary,
        mixHex(textPrimary, "#000000", 0.14),
        mixHex(textPrimary, "#000000", 0.36)
      ];
    }

    return [
      mixHex(appBg, "#000000", 0.18),
      appBg,
      surfaceSoft,
      surfaceElevated,
      mixHex(appBg, textMuted, 0.46),
      textMuted,
      mixHex(textMuted, textSecondary, 0.5),
      textSecondary,
      mixHex(textSecondary, textPrimary, 0.55),
      textPrimary,
      mixHex(textPrimary, "#ffffff", 0.14)
    ];
  };

  Object.values(themes).forEach((theme) => {
    const colors = theme.colors;
    const appBg = normalizeHex(colors["--app-bg"]) || "#f8fafc";
    const textPrimary = normalizeHex(colors["--text-primary"]) || "#111827";
    const accent = normalizeHex(colors["--accent"]) || "#1d9bf0";
    const textMuted = normalizeHex(colors["--text-muted"]) || textPrimary;
    const isLightTheme = relativeLuminance(appBg) > relativeLuminance(textPrimary);

    // Derive accent colors dynamically if not set
    const rgb = hexToRgb(accent);
    if (rgb) {
      const alpha = isLightTheme ? 0.14 : 0.16;
      colors["--accent-soft"] = colors["--accent-soft"] || `rgba(${rgb.r}, ${rgb.g}, ${rgb.b}, ${alpha})`;
    }
    if (isLightTheme) {
      colors["--link-accent"] = colors["--link-accent"] || mixHex(accent, "#000000", 0.08);
      colors["--link-accent-hover"] = colors["--link-accent-hover"] || mixHex(accent, "#000000", 0.18);
    } else {
      colors["--link-accent"] = colors["--link-accent"] || mixHex(accent, "#ffffff", 0.08);
      colors["--link-accent-hover"] = colors["--link-accent-hover"] || mixHex(accent, "#ffffff", 0.18);
    }
    colors["--text-white"] = colors["--text-white"] || (relativeLuminance(accent) > 0.45 ? textPrimary : "#ffffff");

    if (isLightTheme && normalizeHex(colors["--surface-elevated"]) === "#f5f5f5") {
      colors["--surface-elevated"] = mixHex(colors["--surface-card"], colors["--surface-soft"], 0.42);
    }

    const neutralScale = buildNeutralScale(colors, isLightTheme);
    neutralKeys.forEach((key, index) => {
      colors[key] = neutralScale[index];
    });

    const shadow = String(colors["--surface-shadow"] || "").trim();
    if (/^rgba?\(/i.test(shadow)) {
      colors["--surface-shadow"] = `0 18px 38px ${shadow}`;
    }

    colors["--scrollbar-track"] = appBg;
    colors["--scrollbar-thumb"] = mixHex(accent, textMuted, isLightTheme ? 0.5 : 0.42);
    colors["--scrollbar-thumb-hover"] = colors["--link-accent-hover"] || colors["--accent"];
    colors["--scrollbar-thumb-border"] = appBg;
  });
}

normalizeThemePalettes(NamiThemes);

// Expose globally
window.NamiThemeUtils = {
  normalizeThemeColors(colors) {
    const normalizedTheme = { colors: { ...colors } };
    delete normalizedTheme.colors["--accent-soft"];
    delete normalizedTheme.colors["--link-accent"];
    delete normalizedTheme.colors["--link-accent-hover"];
    delete normalizedTheme.colors["--text-white"];
    normalizeThemePalettes({ normalizedTheme });
    return normalizedTheme.colors;
  }
};
window.NamiThemes = NamiThemes;
